package com.bsoftwarefoundation.calculator.Mathformula.Trigonometryformula

class MathTrigonometryHyperbolic{

    fun SinusHyperbolic(X : Double) : Double{
        val result = Math.sinh(X)
        return result
    }

    fun CosinusHyperbolic(X : Double) : Double{
        val result = Math.cosh(X)
        return result
    }

    fun TangenHyperbolic(X : Double) : Double{
        val result = Math.tanh(X)
        return result
    }
}